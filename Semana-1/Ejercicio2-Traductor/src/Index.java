    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String palabra = jTextField1.getText();
        palabra = palabra.toLowerCase();
        Pattern regx = Pattern.compile("^[^<>%$]*$-0-9");
        Matcher matcher = regx.matcher(palabra);

        if (palabra.length() <= 0) {
            JOptionPane.showMessageDialog(null, "No se puede traducir una palabra vacia", "Hey", JOptionPane.ERROR_MESSAGE);
        } else {
            if (matcher.find() == false) {
                JOptionPane.showMessageDialog(null, "No se permiten caracteres especiales, ni numeros", "Hey", JOptionPane.WARNING_MESSAGE);
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

